package com.dajie.message.model.job;

public class PointOnMap {

	private int pointType;
	
	private String name;
	
	private String pointInfo;
	
	private String district;
	
	private String city;
	
	private double longitude;
	
	private double latitude;
	
	private String pointAdress;
	
	private String nameOfPOI;
	
	private String uidOfPOI;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPointInfo() {
		return pointInfo;
	}

	public void setPointInfo(String pointInfo) {
		this.pointInfo = pointInfo;
	}

	public String getDistrict() {
		return district;
	}

	public void setDistrict(String district) {
		this.district = district;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public double getLongitude() {
		return longitude;
	}

	public void setLongitude(double longitude) {
		this.longitude = longitude;
	}

	public double getLatitude() {
		return latitude;
	}

	public void setLatitude(double latitude) {
		this.latitude = latitude;
	}

	public String getPointAdress() {
		return pointAdress;
	}

	public void setPointAdress(String pointAdress) {
		this.pointAdress = pointAdress;
	}

	public String getNameOfPOI() {
		return nameOfPOI;
	}

	public void setNameOfPOI(String nameOfPOI) {
		this.nameOfPOI = nameOfPOI;
	}

	public String getUidOfPOI() {
		return uidOfPOI;
	}

	public void setUidOfPOI(String uidOfPOI) {
		this.uidOfPOI = uidOfPOI;
	}

	public int getPointType() {
		return pointType;
	}

	public void setPointType(int pointType) {
		this.pointType = pointType;
	}
	
	
	
	
}
